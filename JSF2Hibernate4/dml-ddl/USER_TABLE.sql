DROP TABLE USER_TABLE

CREATE TABLE USER_TABLE (
ID INT NOT NULL PRIMARY KEY,
FIRST_NAME VARCHAR(50) NOT NULL,
LAST_NAME VARCHAR(50) NOT NULL,
EMAIL VARCHAR(50) NOT NULL,
PHONE_NUMBER DECIMAL(18, 2) NOT NULL,
STREET_ADDRESS VARCHAR(100) NOT NULL,
CITY VARCHAR(50) NOT NULL,
STATE VARCHAR(50) NOT NULL,
ZIP VARCHAR(50) NOT NULL,
PASSWORD VARCHAR(50) NOT NULL,
FAMILY_GOTHRAM VARCHAR(50) NOT NULL,
PRIMARY_NAKSHATHRAM VARCHAR(50) NOT NULL,
PRIMARY_PADAM VARCHAR(50) NOT NULL,
SPOUSE_NAME VARCHAR(50) NOT NULL,
SPOUSE_NAKSHATHRAM VARCHAR(50) NOT NULL,
SPOUSE_PADAM VARCHAR(50) NOT NULL,
CHILD1_NAME VARCHAR(50) NOT NULL,
CHILD1_NAKSHATHRAM VARCHAR(50) NOT NULL,
CHILD1_PADAM VARCHAR(50) NOT NULL,
CHILD2_NAME VARCHAR(50) NOT NULL,
CHILD2_NAKSHATHRAM VARCHAR(50) NOT NULL,
CHILD2_PADAM VARCHAR(50) NOT NULL,
CHILD3_NAME VARCHAR(50) NOT NULL,
CHILD3_NAKSHATHRAM VARCHAR(50) NOT NULL,
CHILD3_PADAM VARCHAR(50) NOT NULL,
CHILD4_NAME VARCHAR(50) NOT NULL,
CHILD4_NAKSHATHRAM VARCHAR(50) NOT NULL,
CHILD4_PADAM VARCHAR(50) NOT NULL,
IS_ADMIN VARCHAR(50) NOT NULL,
UPDATED_DATE DATETIME NOT NULL,
CREATED_DATE DATETIME NOT NULL,
UPDATED_USER VARCHAR(50) NOT NULL,
CREATED_USER VARCHAR(50) NOT NULL,
CONSTRAINT UC_USER_CONSTRAINT UNIQUE (FIRST_NAME, LAST_NAME, EMAIL, PHONE_NUMBER),
)